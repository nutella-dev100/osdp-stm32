/* USER CODE BEGIN 4 */
void stateMachineHandler(CP_SC_STATE* state){
    switch(*state){
        case CP_SC_IDLE:
            *state = CP_SC_CHLNG_SENT;
            break;
        case CP_SC_CHLNG_SENT:
            response_rec = 0;
            osdp_pkt_header *buf_chlng = scs11pkt(1);
            size_t len_scs11 = (buf_chlng->len_msb << 8) | (buf_chlng->len_lsb);
            transmitPacket(buf_chlng, len_scs11 + 1);
            free(buf_chlng);
            *state = CP_SC_CHLNG_WAIT_RESP;
            break;
        case CP_SC_CHLNG_WAIT_RESP:
            if(response_rec) *state = CP_SC_SCRYPT_SENT;
            //if(elapse > TIMEOUT)  advance the state
            break;
        case CP_SC_SCRYPT_SENT:
            response_rec = 0;
            osdp_pkt_header *buf_scrypt = scs13pkt(rndb, s_enc, 1);
            size_t len_scs13 = (buf_scrypt->len_msb << 8) | (buf_scrypt->len_lsb);
            transmitPacket(buf_scrypt, len_scs13 + 1);
            free(buf_scrypt);
            *state = CP_SC_SCRYPT_WAIT_RESP;
            break;
        case CP_SC_SCRYPT_WAIT_RESP:
            if(response_rec) *state = CP_SC_SECURE;
            //if(elapse > TIMEOUT)  advance the state
            break;
        case CP_SC_SECURE:
            response_rec = 0;
            HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_14);
            break;
        default:
            break;
    }
}

