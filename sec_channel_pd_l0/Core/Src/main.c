/* USER CODE BEGIN 4 */
void stateMachineHandler(PD_SC_STATE* state){
    switch (*state)
    {
    case PD_SC_IDLE:
        if(response_rec){
        	*state = PD_CHLNG_REC;
        	response_rec = 0;
        }
        break;
    case PD_CHLNG_REC:
        //response_rec = 0;
        *state = PD_CCRYPT_SENT;
        break;
    case PD_CCRYPT_SENT:
        osdp_pkt_header *buf_chlng = scs12pkt(1);
        size_t len_scs12 = (buf_chlng->len_msb << 8)|(buf_chlng->len_lsb);
        transmitPacket(buf_chlng, len_scs12 + 1);
        free(buf_chlng);
        *state = PD_CCRYPT_WAIT_RESP;
        break;
    case PD_CCRYPT_WAIT_RESP:
        if(response_rec){
            *state = PD_SCRYPT_REC;
            response_rec = 0;
        }
        break;
    case PD_SCRYPT_REC:
        osdp_pkt_header *buf_resp = scs14pkt(1);
        size_t len_scs14 = (buf_resp->len_msb << 8)|(buf_resp->len_lsb);
        transmitPacket(buf_resp, len_scs14 + 1);
        free(buf_resp);
        *state = PD_SECURE;
    case PD_SECURE:
        //HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
    	osdp_pkt_header *test = scs16pkt();
    	size_t len_scs16 = (test->len_msb << 8)|(test->len_lsb);
    	transmitPacket(test, len_scs16 + 1);
    	free(test);
    	HAL_Delay(500);
        break;

    default:
        break;
    }
}

